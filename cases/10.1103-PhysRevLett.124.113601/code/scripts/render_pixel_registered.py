def _configure_style() -> None:
    mpl.rcParams.update(
        {
            "font.family": "sans-serif",
            "font.sans-serif": ["Helvetica", "Arial", "DejaVu Sans"],
            "mathtext.fontset": "cm",
            "axes.linewidth": 0.8,
            "axes.edgecolor": "#262626",
            "axes.labelcolor": "#262626",
            "xtick.color": "#262626",
            "ytick.color": "#262626",
            "xtick.direction": "in",
            "ytick.direction": "in",
            "xtick.top": True,
            "ytick.right": True,
            "legend.frameon": True,
            "legend.fancybox": False,
            "legend.edgecolor": "#262626",
            "legend.facecolor": "white",
            "legend.framealpha": 1.0,
            "pdf.fonttype": 42,
            "svg.fonttype": "none",
        }
    )


def _figure(kind: str) -> plt.Figure:
    width, height = PAPER_GEOMETRY[kind]["pixels"]
    # The epsilon prevents Agg from truncating an integer pixel because of a
    # binary floating-point representation just below the requested size.
    return plt.figure(figsize=((width + 1e-6) / DPI, (height + 1e-6) / DPI), dpi=DPI, facecolor="white")


def _save(fig: plt.Figure, stem: str) -> dict[str, object]:
    FIGURE_DIR.mkdir(parents=True, exist_ok=True)
    png = FIGURE_DIR / f"{stem}.png"
    pdf = FIGURE_DIR / f"{stem}.pdf"
    fig.savefig(png, dpi=DPI, facecolor="white")
    fig.savefig(pdf, dpi=DPI, facecolor="white")
    plt.close(fig)
    from PIL import Image

    size = Image.open(png).size
    return {"png": str(png.relative_to(OUTPUT_ROOT)), "pdf": str(pdf.relative_to(OUTPUT_ROOT)), "size": list(size)}


def _read_rows(name: str) -> list[dict[str, str]]:
    with (DATA_DIR / name).open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def _series(rows: list[dict[str, str]], x: str, y: str, **filters: object) -> tuple[np.ndarray, np.ndarray]:
    selected = [
        row
        for row in rows
        if all(np.isclose(float(row[key]), float(value)) if _is_number(value) else row[key] == str(value) for key, value in filters.items())
    ]
    return np.asarray([float(row[x]) for row in selected]), np.asarray([float(row[y]) for row in selected])


def _is_number(value: object) -> bool:
    return isinstance(value, (float, int, np.floating, np.integer))


def _paper_axes(ax: plt.Axes, tick_size: float, label_size: float, *, tick_length: float = 4.0) -> None:
    for spine in ax.spines.values():
        spine.set_visible(True)
        spine.set_linewidth(0.8)
        spine.set_color("#262626")
    ax.tick_params(labelsize=tick_size, width=0.8, length=tick_length, pad=4)
    ax.xaxis.label.set_size(label_size)
    ax.yaxis.label.set_size(label_size)


def render_fig2() -> dict[str, object]:
    rows = _read_rows("fig2_state_thresholds.csv")
    fig = _figure("fig2")
    ax = fig.add_axes(PAPER_GEOMETRY["fig2"]["main"])
    gaa_x, gaa_y = _series(rows, "energy_over_J", "eta_c_over_J", model="GAA")
    aa_x, aa_y = _series(rows, "energy_over_J", "eta_c_over_J", model="AA")
    ax.plot(gaa_x, gaa_y, color="#0000FF", lw=2.5, ls=":", label="GAA")
    ax.plot(aa_x, aa_y, color="#FF0000", lw=2.5, ls="-.", label="AA")
    ax.set(xlim=(-2.7, 2.7), ylim=(-0.01, 0.32), xticks=[-2, -1, 0, 1, 2], yticks=[0, 0.1, 0.2, 0.3])
    ax.set_xlabel(r"$\epsilon_{\alpha}/J$", fontsize=21, labelpad=4)
    ax.set_ylabel(r"$\eta_c$", fontsize=21, labelpad=8)
    _paper_axes(ax, 20, 21, tick_length=6)
    legend = ax.legend(loc="lower left", bbox_to_anchor=(0.035, 0.075), fontsize=20, handlelength=1.6, handletextpad=0.25, borderpad=0.2, labelspacing=0.25)
    legend.get_frame().set_linewidth(0.8)

    inset = fig.add_axes(PAPER_GEOMETRY["fig2"]["inset"])
    alpha, ipr = _series(rows, "alpha", "ipr", model="GAA")
    inset.plot(alpha, ipr, color="black", lw=1.8)
    inset.axvline(233, color="#808080", lw=1.6, ls="--", dashes=(4, 4))
    inset.text(166, 0.55, r"$\epsilon_c/J\simeq0.44$", color="#0000FF", fontsize=18)
    inset.set(xlim=(0, 377), ylim=(-0.03, 0.75), xticks=[0, 100, 200, 300], yticks=[0, 0.2, 0.4, 0.6])
    inset.set_xlabel(r"$\alpha$", fontsize=20, labelpad=0)
    inset.set_ylabel(r"$\mathrm{IPR}(\alpha)$", fontsize=20, labelpad=5)
    _paper_axes(inset, 17, 20, tick_length=4)
    return _save(fig, "fig2_pixel_registered")


