    ax_a.text(0.035, 0.83, r"(a)   $\eta_c$", transform=ax_a.transAxes, fontsize=15)
    ax_a.axvline(2.0, color="black", lw=1.5, ls="--", dashes=(5, 4))

    ax_ar = ax_a.twinx()
    ax_ar.plot(chi, f1, color="#FF0000", lw=1.5, ls="-.", label=r"$f_1$")
    ax_ar.set_yscale("log")
    ax_ar.set(ylim=(1e-2, 1e4), yticks=[1e-2, 1e0, 1e2, 1e4])
    ax_ar.set_ylabel(r"$f_1$", fontsize=16, labelpad=4)
