    channels = _read_rows("fig3_channels.csv")
    fig = _figure("fig3")

    ax_a = fig.add_axes(PAPER_GEOMETRY["fig3"]["a"])
    chi, eta = _series(threshold, "chi_over_J", "eta_c_over_J")
    _, f1 = _series(threshold, "chi_over_J", "f1")
