def render_fig4() -> dict[str, object]:
    photon = _read_rows("fig4a_photon_number.csv")
    landscape = _read_rows("fig4b_threshold_landscape.csv")
    fig = _figure("fig4")

    ax_a = fig.add_axes(PAPER_GEOMETRY["fig4"]["a"])
    for chi_value, color, linestyle, dashes in (
        (0.5, "#0000FF", "-.", (3, 2, 1, 2)),
        (1.0, "#FF0000", "--", (5, 4)),
        (2.03, "#008000", "-", None),
    ):
        eta, photons = _series(photon, "eta_over_J", "photon_number", chi_over_J=chi_value)
        line, = ax_a.plot(eta, photons, color=color, lw=1.0, ls=linestyle, label=rf"$\chi/J={chi_value:g}$")
        if dashes:
            line.set_dashes(dashes)
    # The published panel suppresses y tick labels; its clipping points fix the
    # visible photon-number window to 0..5.
    ax_a.set(xlim=(0, 0.25), ylim=(0, 5.0), xticks=[0, 0.1, 0.2], yticks=[0, 1, 2, 3, 4, 5])
    ax_a.set_xticklabels(["0", "0.1", "0.2"])
    ax_a.set_yticklabels([])
    ax_a.set_xlabel(r"$\eta/J$", fontsize=13, labelpad=2)
    _paper_axes(ax_a, 11, 13, tick_length=3)
    ax_a.text(0.02, 0.90, "(a)", transform=ax_a.transAxes, fontsize=12)
    legend = ax_a.legend(loc="upper left", bbox_to_anchor=(0.13, 1.01), fontsize=10, handlelength=2.2, handletextpad=0.25, borderpad=0.2, labelspacing=0.05)
    legend.get_frame().set_linewidth(0.6)

    ax_b = fig.add_axes(PAPER_GEOMETRY["fig4"]["b"])
    colors = {0.0: "#FFFF00", 0.5: "#00E5E5", 1.5: "#00BDBD", 1.9: "#FF0000", 2.03: "#0000FF"}
    for chi_value in (0.0, 0.5, 1.5, 1.9, 2.03):
        gamma_c, eta_c = _series(landscape, "gamma_c", "eta_c_over_J", chi_over_J=chi_value)
        ax_b.fill_between(gamma_c, 0, eta_c, color=colors[chi_value], lw=0, label=rf"$\chi/J={chi_value:g}$")
    gamma = (np.sqrt(5.0) - 1.0) / 2.0
    for label, position in ((r"$2\gamma-1$", 2 * gamma - 1), (r"$1-\gamma$", 1 - gamma), (r"$\gamma$", gamma), (r"$2(1-\gamma)$", 2 * (1 - gamma))):
        ax_b.annotate("", xy=(position, 0.165), xytext=(position - 0.055, 0.255), arrowprops={"arrowstyle": "->", "color": "black", "lw": 0.8})
        ax_b.text(position - 0.16, 0.26, label, fontsize=9)
    ax_b.set(xlim=(0, 1), ylim=(0, 0.6), xticks=[0, 0.5, 1], yticks=[0, 0.3, 0.6])
    ax_b.set_xticklabels(["0", "0.5", "1"])
    ax_b.set_yticklabels(["0", "0.3", "0.6"])
    ax_b.set_xlabel(r"$\gamma_c$", fontsize=13, labelpad=1)
    ax_b.set_ylabel(r"$\eta_c$", fontsize=13, labelpad=2)
    _paper_axes(ax_b, 11, 13, tick_length=3)
    ax_b.text(0.02, 0.90, "(b)", transform=ax_b.transAxes, fontsize=12)
    legend = ax_b.legend(loc="upper right", bbox_to_anchor=(0.99, 0.99), fontsize=8.5, handlelength=1.25, handletextpad=0.25, borderpad=0.15, labelspacing=0.0)
    legend.get_frame().set_linewidth(0.6)
    return _save(fig, "fig4_pixel_registered")


def render_figs1() -> dict[str, object]:
    rows = _read_rows("figs1_density_profiles.csv")
    fig = _figure("figs1")
    panels = (("a", 0.0), ("b", 0.5), ("c", 1.0), ("d", 1.9), ("e", 2.03))
    limits = {
        0.0: ((0, 377), (0, 0.015), [0, 0.005, 0.01, 0.015]),
        0.5: ((0, 377), (0, 0.02), [0, 0.01, 0.02]),
        1.0: ((0, 377), (0, 0.06), [0, 0.02, 0.04, 0.06]),
        1.9: ((0, 377), (0, 0.2), [0, 0.1, 0.2]),
        2.03: ((65, 90), (0, 1.0), [0, 0.5, 1.0]),
    }
    for panel, chi_value in panels:
        ax = fig.add_axes(PAPER_GEOMETRY["figs1"][panel])
        sites, normal = _series(rows, "site", "probability", chi_over_J=chi_value, phase="normal")
        _, superradiant = _series(rows, "site", "probability", chi_over_J=chi_value, phase="superradiant")
        ax.plot(sites, normal, color="#0000FF", lw=1.2, label=r"$\eta<\eta_c$" if chi_value < 2 else r"$\eta=0$")
        red, = ax.plot(sites, superradiant, color="#FF0000", lw=1.0, ls=":", label=r"$\eta>\eta_c$" if chi_value < 2 else r"$\eta>0$")
        if chi_value == 2.03:
            red.set_marker("^")
            red.set_markevery(2)
            red.set_markersize(4)
        xlim, ylim, yticks = limits[chi_value]
        ax.set(xlim=xlim, ylim=ylim, yticks=yticks)
        ax.set_xlabel(r"$j$", fontsize=13, labelpad=1)
        ax.set_ylabel(r"$|\phi_0^j|^2$", fontsize=13, labelpad=2)
        _paper_axes(ax, 11, 13, tick_length=3)
        ax.text(0.03, 0.86, rf"({panel})  $\chi/J={chi_value:g}$", transform=ax.transAxes, fontsize=12)
        legend = ax.legend(loc="upper right", fontsize=10.5, handlelength=2.0, handletextpad=0.25, borderpad=0.2, labelspacing=0.1)
        legend.get_frame().set_linewidth(0.6)
        if chi_value in (0.0, 0.5, 1.0):
            ax.ticklabel_format(axis="y", style="sci", scilimits=(-2, -2), useMathText=True)
            ax.yaxis.get_offset_text().set_size(11)
    return _save(fig, "figs1_pixel_registered")


def main() -> int:
    _configure_style()
    artifacts = {
        "fig2": render_fig2(),
        "fig3": render_fig3(),
        "fig4": render_fig4(),
        "figs1": render_figs1(),
    }
    expected = {key: list(value["pixels"]) for key, value in PAPER_GEOMETRY.items()}
    checks = {key: artifacts[key]["size"] == expected[key] for key in artifacts}
    payload = {
        "status": "passed" if all(checks.values()) else "failed",
        "renderer": "independent_csv_to_paper_registered_matplotlib",
        "source_pixels_used": False,
        "dpi": DPI,
        "artifacts": artifacts,
        "geometry_checks": checks,
    }
    CHECK_DIR.mkdir(parents=True, exist_ok=True)
    (CHECK_DIR / "pixel_registered_render.json").write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    print(json.dumps(payload, indent=2))
    return 0 if payload["status"] == "passed" else 1


if __name__ == "__main__":
    raise SystemExit(main())
